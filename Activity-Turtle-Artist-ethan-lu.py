# Star maker function
def drawStar(length: int, color):
    t.pendown()
    if color.lower().strip("!.,? ") == "red" or color.lower().strip("!.,? ") == "orange" or color.lower().strip("!.,? ") or "yellow" or color.lower().strip("!.,? ") == "white" or color.lower().strip("!.,? ") == "blue" or color.lower().strip("!.,? ") == "purple":
        t.fillcolor(color)
        t.begin_fill()
        t.setheading(90)
        t.color(color)
        t.fillcolor(color)
        t.pendown()
        t.begin_fill()
        t.right(15)
        t.forward(length)
        t.right(150)
        t.forward(length)
        t.left(75)
        t.forward(length)
        t.right(150)
        t.forward(length)
        t.left(75)
        t.forward(length)
        t.right(155)
        t.forward(length)
        t.left(105)
        t.forward(length)
        t.right(155)
        t.forward(length + (length/10))
        t.left(75)
        t.forward(length)
        t.right(155)
        t.forward(length)
        t.end_fill()

